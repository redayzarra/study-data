"""
Python - is a dynamic language meaning we don't have to declare types
"""
n = 0
print("n =", n)

n = 'abc'
print('n =', n)


"""
Multiple Assignments - declare multiple variables side by side
"""
n, m = 1, "abc"
print(n, m)


"""
Incrementing - adding a constant value to the variable on every iteration
"""
n = n + 1

n += 1

"""
Null - the null value in Python is just called None
"""
y = 6
y = None
print('y = ', y)